// JavaScript code for handling dropdown changes
document.addEventListener('DOMContentLoaded', function() {
    const regionSelect = document.getElementById('region');
    const districtSelect = document.getElementById('district');
    const wardSelect = document.getElementById('ward');
    const villageSelect = document.getElementById('village');

    // Populate regions on page load
    if (regionSelect) {
        Object.keys(locations).forEach(region => {
            regionSelect.innerHTML += `<option value="${region}">${region}</option>`;
        });
    }

    // Region change event
    if (regionSelect) {
        regionSelect.addEventListener('change', function() {
            const region = this.value;
            
            // Reset downstream selects
            districtSelect.innerHTML = '<option value="">-- Select District --</option>';
            wardSelect.innerHTML = '<option value="">-- Select Ward --</option>';
            villageSelect.innerHTML = '<option value="">-- Select Village --</option>';
            
            if (region && locations[region]) {
                Object.keys(locations[region]).forEach(district => {
                    districtSelect.innerHTML += `<option value="${district}">${district}</option>`;
                });
            }
            
            // Enable district select
            districtSelect.disabled = !region;
            wardSelect.disabled = true;
            villageSelect.disabled = true;
        });
    }

    // District change event
    if (districtSelect) {
        districtSelect.addEventListener('change', function() {
            const region = regionSelect.value;
            const district = this.value;
            
            // Reset downstream selects
            wardSelect.innerHTML = '<option value="">-- Select Ward --</option>';
            villageSelect.innerHTML = '<option value="">-- Select Village --</option>';
            
            if (region && district && locations[region] && locations[region][district]) {
                Object.keys(locations[region][district]).forEach(ward => {
                    wardSelect.innerHTML += `<option value="${ward}">${ward}</option>`;
                });
            }
            
            // Enable ward select
            wardSelect.disabled = !district;
            villageSelect.disabled = true;
        });
    }

    // Ward change event
    if (wardSelect) {
        wardSelect.addEventListener('change', function() {
            const region = regionSelect.value;
            const district = districtSelect.value;
            const ward = this.value;
            
            // Reset village select
            villageSelect.innerHTML = '<option value="">-- Select Village --</option>';
            
            if (region && district && ward && 
                locations[region] && 
                locations[region][district] && 
                locations[region][district][ward]) {
                
                locations[region][district][ward].forEach(village => {
                    villageSelect.innerHTML += `<option value="${village}">${village}</option>`;
                });
            }
            
            // Enable village select
            villageSelect.disabled = !ward;
        });
    }
});